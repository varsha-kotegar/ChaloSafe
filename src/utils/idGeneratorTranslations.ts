export function getIdGeneratorTranslation(key: keyof typeof idGeneratorTranslations, language: Language): string {
  const translation = idGeneratorTranslations[key];
  if (!translation) {
    console.warn(`Translation key "${key}" not found in idGeneratorTranslations`);
    return key;
  }
  return translation[language] || translation.en || key;
}